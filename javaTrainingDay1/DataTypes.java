public class DataTypes{
	
	public static void main(String[]args){
			
		//DECLARATION
		
		int number;
		float float_number;
		double dbl_no;
		long long_int;
		char word;
		boolean flag;
		

		//INITIALIZATION

		number=4;
		float_number= 3.3f;
		dbl_no=66.66666;
		long_int=9999999;
		word='a';
		flag=false;
		
		//PRINTING VALUES		
		
		System.out.println("Integer :"+number);
		System.out.println("Float Number :"+float_number);
		System.out.println("Double :"+dbl_no);
		System.out.println("Long Int :"+long_int);
		System.out.println("Character :"+word);
		System.out.println("Boolean :"+flag);	
		
	}
	
}